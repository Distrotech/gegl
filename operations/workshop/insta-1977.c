static void
gegl_insta_1977_prepare (GeglOperation *operation)
{
  gegl_operation_set_format (operation, "input",
                             babl_format ("R'G'B' u8"));
  gegl_operation_set_format (operation, "output",
                             babl_format ("R'G'B' u8"));
}

static gboolean
gegl_insta_1977_process (GeglOperation       *operation,
                         void                *in_buf,
                         void                *out_buf,
                         glong                n_pixels,
                         const GeglRectangle *roi,
                         gint                 level)
{
  guint8 *dest =out_buf;
  guint8 *src = in_buf;
  glong   i;

  for (i = 0; i < n_pixels; i++)
    {
      const glong offset = i * 3;

      dest[offset + 0] = nine_r[src[offset + 0]];
      dest[offset + 1] = nine_g[src[offset + 1]];
      dest[offset + 2] = nine_b[src[offset + 2]];

      dest[offset + 0] = nine_a[dest[offset + 0]];
      dest[offset + 1] = nine_a[dest[offset + 1]];
      dest[offset + 2] = nine_a[dest[offset + 2]];
    }

  return TRUE;
}

static void
gegl_op_class_init (GeglOpClass *klass)
{
  GeglOperationClass            *operation_class;
  GeglOperationPointFilterClass *point_filter_class;

  operation_class    = GEGL_OPERATION_CLASS (klass);
  point_filter_class = GEGL_OPERATION_POINT_FILTER_CLASS (klass);

  operation_class->prepare    = gegl_insta_1977_prepare;
  point_filter_class->process = gegl_insta_1977_process;

  gegl_operation_class_set_keys (operation_class,
    "name",        "gegl:insta-1977",
    "title",       _("Instagram's 1977 filter"),
    "categories",  "enhance",
    NULL);
}

